void main() {
  // Na linguagem Dart existe o double para armazenar numeros reais.
  double real = 1.00 * 2;
  print(real);

  // execuções do tipo divisão com int e com resultados reais.
  int numero = 5;
  int numero1 = 2;
  double divisao = numero / numero1;
  print(numero / numero1);
  // testando numeros fracionários.
  double resultfra = divisao * 4;
  print(resultfra);
}
