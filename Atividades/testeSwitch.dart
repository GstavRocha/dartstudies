void main() {
  int dia = 2;

  switch (dia) {
    case 1:
      print("Segunda");
      break;
    case 2:
      print("Terça");
      break;
    case 3:
      print("Quarta");
      break;
    case 4:
      print("Quinta");
      break;
    case 5:
      print("Sexta");
      break;
    case 6:
      print("Sabado");
      break;
    case 7:
      print("Domingo");
      break;
    default:
      print("Selecione um dia da semana válido");
      break;
  }
}
